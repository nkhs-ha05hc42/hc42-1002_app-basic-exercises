// 変数aを定義
const a = 10

// 境界値を設定し、各演算のTrue/Falseとなるパターンを出力

// 大なり (>)
console.log("a > 10:", a > 10)    // False
console.log("a > 9:", a > 9)      // True

// 大なりイコール (>=)
console.log("a >= 10:", a >= 10)  // True
console.log("a >= 11:", a >= 11)  // False

// 小なり (<)
console.log("a < 10:", a < 10)    // False
console.log("a < 11:", a < 11)    // True

// 小なりイコール (<=)
console.log("a <= 10:", a <= 10)  // True
console.log("a <= 9:", a <= 9)    // False
